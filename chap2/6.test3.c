/*
给输出的内容加⼀个漂亮的框框
*/
#include <stdio.h>

int main() {
    char s[100], t[100];
    scanf("%[^\n]", s);
    int n = sprintf(t, "| %s |", s);
    for (int i = 0; i < n; i++)
        printf("-");
    printf("\n");

    printf("%s\n", t);

    for (int i = 0; i < n; i++)
        printf("-");
    printf("\n");
    return 0;
}